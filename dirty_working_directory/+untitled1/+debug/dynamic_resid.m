function [lhs, rhs] = dynamic_resid(y, x, params, steady_state)
T = NaN(5, 1);
lhs = NaN(6, 1);
rhs = NaN(6, 1);
T(1) = exp(y(8))*params(5)*exp(y(11))^(1+params(6));
T(2) = exp(y(8))*exp(y(12))/(exp(y(18))*exp(y(14)));
T(3) = exp(y(10))*exp(y(3))^params(3);
T(4) = exp(y(11))^(1-params(3));
T(5) = T(3)*T(4);
lhs(1) = T(1);
rhs(1) = (1-params(3))*exp(y(7));
lhs(2) = exp(y(9));
rhs(2) = params(1)*T(2)*(params(3)*exp(y(18))*exp(y(13))+exp(y(9))*(1-params(4)));
lhs(3) = exp(y(7));
rhs(3) = T(5);
lhs(4) = exp(y(9));
rhs(4) = exp(y(12))*(exp(y(7))-exp(y(8)))+(1-params(4))*exp(y(3));
lhs(5) = y(10);
rhs(5) = params(2)*y(4)+params(7)*y(6)+x(1);
lhs(6) = y(12);
rhs(6) = y(4)*params(7)+params(2)*y(6)+x(2);
end
